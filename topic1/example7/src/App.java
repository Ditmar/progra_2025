    public static Collection<Integer> repeatNumbersHash(Integer[] list) {
        HashMap<Integer, Integer> results = new HashMap<>();
        // O(n)
        for (Integer i = 0; i < list.length; i++) {
            if (results.containsKey(list[i])) {
                Integer value = results.get(list[i]);
                value++;
                results.put(list[i], value);
            } else {
                results.put(list[i], 1);
            }
        }
        return results.values();
    }

    public static ArrayList<Integer> repeatNumbersList(Integer[] list) {
        ArrayList<Integer> result = new ArrayList<>();
        // O(n * m) c on m < n
        for (Integer i = 0; i < list.length; i++) {
            Integer count = 1;
            if (i + 1 > list.length) {
                break;
            }
            for (Integer j = i + 1; j < list.length; j++) {
                if (list[i] == list[j]) {
                    list[j] = null;
                    count++;
                }
            }
            if (list[i] != null) {
                result.add(count);
            }

        }
        return result;
    }
